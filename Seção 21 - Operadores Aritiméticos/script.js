//Adição/Concatenação:
console.log(1 + 1);
console.log('Eduardo ' + 'Olimpio'); //Concatenação

console.log('Eduardo ' + 'Olimpio' + ',' + ' idade: ' + 36 + ' anos.' );

//Subtração:

console.log(1 - 1);
console.log('1' - 1);

//Multiplicação:

console.log(2 * 10);
console.log(10 * 5);

//Divisão:
console.log(10 / 2);
console.log(50 / 10);

//Resto da divisão:
console.log(4 % 2);
console.log(57 % 5);

//Encontar número par ou ímpar:
console.log(4 % 2); // se o resultado for 0 é par se for 1 é impar.